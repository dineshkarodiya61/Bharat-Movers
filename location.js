function filterLocations(query) {
  return indiaLocations.filter(loc =>
    loc.city.toLowerCase().startsWith(query.toLowerCase())
  );
}

function createSuggestions(inputElement) {
  inputElement.addEventListener("input", function () {
    const value = this.value.trim();
    closeAllSuggestions();
    if (!value) return;
    const matches = filterLocations(value);
    if (!matches.length) return;
    const list = document.createElement("div");
    list.className = "autocomplete-items";
    this.parentNode.appendChild(list);
    matches.forEach(loc => {
      const item = document.createElement("div");
      const displayText = `${loc.city} – ${loc.pin} – ${loc.district}`;
      item.innerHTML = displayText;
      item.addEventListener("click", () => {
        inputElement.value = displayText;
        closeAllSuggestions();
      });
      list.appendChild(item);
    });
  });
  function closeAllSuggestions() {
    document.querySelectorAll(".autocomplete-items").forEach(el => el.remove());
  }
  document.addEventListener("click", e => {
    if (e.target !== inputElement) closeAllSuggestions();
  });
}
createSuggestions(document.getElementById("pickupCity"));
